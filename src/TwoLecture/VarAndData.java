package TwoLecture;

public class VarAndData {
    public static void main(String[] args) {
        int myNumber = 856;
        int yourNumbers;
        yourNumbers = myNumber;
        System.out.println(yourNumbers);
        yourNumbers = 45;
        System.out.println(yourNumbers);

        float myFloat = 5.45f;
        double myDouble = 5.45464546;
        System.out.println(myFloat);
        System.out.println(myDouble);

        boolean isVegetarian = false;
        System.out.println(isVegetarian);

        String wishes = "Good Morning";
        System.out.println(wishes);

        char myCharacters = 'K';
        System.out.println(myCharacters);
    }
}
